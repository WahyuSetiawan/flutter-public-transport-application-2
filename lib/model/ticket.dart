class Ticket {
  final String from;
  final String dateFrom;

  final String to;
  final String dateTo;

  final int estimationTime;

  final String passenger;
  final String date;
  final String bonus;
  final int bus_number;
  final String seat;
  final int tickets;
  final String arrival;

  Ticket(
      {this.from,
      this.dateFrom,
      this.to,
      this.dateTo,
      this.estimationTime,
      this.passenger,
      this.date,
      this.bonus,
      this.bus_number,
      this.seat,
      this.tickets,
      this.arrival});
}
